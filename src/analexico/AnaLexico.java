    public ArrayList gerarEntradas(ArrayList entradas, String entrada){
        
        System.out.println("Nova palavra a ser analisada: " + entrada);
        
        entradas.clear();
        
        for(int i = 0; i < entrada.length(); i++){
            if(i != 0)
                entradas.add(entrada.substring(0, i));
            
        }
        
        entradas.add(entrada);
        
        System.out.println("entradas: " + entradas);
        
        return entradas;
    }
    
