    public int verifica(String entrada, ArrayList<ArrayList<ArrayList<Integer>>> matriz, int estadoAtual){
        
        if(entrada.length() == 0){
            
            if(matriz.get(estadoAtual).get(0).size() > 1){
                
                return 0;
            }else{
                
                return 1;
            }
            
        }
        
        
        if(PosFixo.alfabeto.indexOf(entrada.charAt(0)) != -1)
            return verifica((entrada.substring(1)), matriz, matriz.get(estadoAtual).get(PosFixo.alfabeto.indexOf(entrada.charAt(0))+1).get(0));
        
        return 1;
    }
    
    
    
    public ArrayList gerarMatriz(ArrayList<String> tokenLista){
        
        ArrayList<ArrayList<ArrayList<Integer>>> matrizAfn = new ArrayList<>();
        ArrayList<ArrayList<ArrayList<Integer>>> matrizAfd = new ArrayList<>();
        
        PosFixo p = new PosFixo();
        Automato a = new Automato();
        
        
        String regex = tokenLista.get(0);
        
        
        regex = p.infixa_para_posfixa(regex);
        regex = regex.replace(" ", "");
        
        
        a.er_para_afn(regex);
        
        matrizAfn = p.criarMatrizAfn(a);
        
        matrizAfd = p.criarMatrizAfd(a.renomeiaMatriz(a.afn_para_afd(matrizAfn)));
        
        System.out.println("AFD: ");
        for(int i = 0; i < matrizAfd.size(); i++){
            for(int j = 0; j < matrizAfd.get(0).size(); j++){
                System.out.print(" " + matrizAfd.get(i).get(j));
            }
            System.out.println("");
        }
        
        return matrizAfd;
    }
    
