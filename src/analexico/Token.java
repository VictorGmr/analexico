    public void inicia(){
        DefaultTableModel model = new DefaultTableModel();
        
        String[] ident = new String[2];
        ident[0] = "Padrão (Expressão Regular)";
        ident[1] = "Token";
        
        
        model.setColumnIdentifiers(ident);
        model.setRowCount(AnaLexico.tokenLista.size());
        
        for(int i = 0; i < AnaLexico.tokenLista.size(); i++){
            for(int j = 0; j < 2; j++){
                if(j == 1){
                    model.setValueAt("<" + AnaLexico.tokenLista.get(i).get(j) + ">", i, j);
                }else{
                    model.setValueAt(AnaLexico.tokenLista.get(i).get(j), i, j);
                }
                
            }
        }
        
        jTable1.setModel(model);
    }
    
