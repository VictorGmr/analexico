    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        
        
        if((jTextField1.getText().isEmpty()) || (jTextField2.getText().isEmpty())){
            JOptionPane.showMessageDialog(null, "Preencha os campos de Padrão e Token!");
        }else{
        
            AnaLexico.tokenLista.add(new ArrayList<>());

            AnaLexico.tokenLista.get(AnaLexico.tokenLista.size() - 1).add(jTextField1.getText());
            AnaLexico.tokenLista.get(AnaLexico.tokenLista.size() - 1).add(jTextField2.getText());

            DefaultTableModel model = new DefaultTableModel();

            String[] ident = new String[2];
            ident[0] = "Padrão (Expressão Regular)";
            ident[1] = "Token";


            model.setColumnIdentifiers(ident);
            model.setRowCount(AnaLexico.tokenLista.size());

            for(int i = 0; i < AnaLexico.tokenLista.size(); i++){
                for(int j = 0; j < 2; j++){
                    if(j == 0){
                        model.setValueAt(AnaLexico.tokenLista.get(i).get(j), i, j);
                    }else if(j == 1){
                        model.setValueAt("<" + AnaLexico.tokenLista.get(i).get(j) + ">", i, j);
                    }
                }
            }

            jTable1.setModel(model);

            jTextField1.setText("");
            jTextField2.setText("");
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

